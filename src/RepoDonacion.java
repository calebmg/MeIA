    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        //Contador de bloques para el reporte
        int Aux=1;
        for (int i = 1; i < 100; i++) {
            File ArchivoMaestro = new File("C:\\MEIA\\Donaciones_" + i + ".txt");
            if(ArchivoMaestro.exists()!=true){
                break;
            }
            else{Aux ++;}
        }

        //FECHAS
        int MesI=Integer.parseInt(IMes.getText());
        int MesF=Integer.parseInt(FMes.getText());
        int AnioI=Integer.parseInt(IAnio.getText());
        int AnioF=Integer.parseInt(FAnio.getText());
        
        String reporte="";

        //Lector de blocques
        try{
        for (int i = 1; i < Aux; i++) {
            File ArchivoMaestro = new File("C:\\MEIA\\Donaciones_" + i + ".txt");
            

                FileReader a= new FileReader(ArchivoMaestro);
                BufferedReader br = new BufferedReader(a);
                String Linea;
                while((Linea=br.readLine())!=null){
                    String [] Sepa = Linea.split(",");
                    //FECHA EN EL 7 para reporte filtrado//FECHA EN EL 7 para reporte filtrado
                    String fecha=Sepa[7];
                    String [] Espe=fecha.split("/");
                    //Mes Espe[1], AÃ±o Espe[2];
                    //MOstrar usuario, material,fecha de donacion, peso, descripcion y evento
                    int mes=Integer.parseInt(Espe[1]);
                    int anio=Integer.parseInt(Espe[2]);
                    if ((anio<=AnioF)&&(anio>=AnioI)&&(mes<=MesF)&&(mes>=MesI)) {
                        reporte+=Linea+ "\n";
                    }
                }

            }
        File ArchivoEscrito = new File("C:\\MEIA\\Reporte_" + reportes + ".txt");
        
            ArchivoEscrito.createNewFile();
            Writer escritor = new FileWriter(ArchivoEscrito, true);
                escritor.write(reporte);
                escritor.close();
                reportes++;
        }
        catch (Exception e) {};
    }//GEN-LAST:event_jButton1ActionPerformed

